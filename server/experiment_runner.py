"""
Experiment Runner
=================

Reads synthetic data from fs/runs/{run_id}/data/ (generated by /synthetic/from-pdf),
directly maps rows to Tamarind Bio jobs based on available columns, submits, polls,
downloads, and validates.

No Claude planning step — synthetic data columns are mapped directly to Tamarind
tool settings based on what data is available.
"""

from __future__ import annotations

import json
import logging
import time
import uuid
from pathlib import Path

from anthropic import Anthropic

from synthetic_data_gen.paper_parser import PaperParser
from tamarind_client import TamarindClient

logger = logging.getLogger(__name__)

EXPERIMENTS_DIR = Path(__file__).parent / "sample_data" / "experiments"
FS_RUNS_DIR = Path(__file__).parent / "fs" / "runs"


# ── Column-to-Tamarind mapping ──────────────────────────────────────────
# Maps synthetic data column names to Tamarind tool settings.
# If a row has the right columns, we can submit it to the corresponding tool.

TOOL_COLUMN_MAP = {
    "alphafold": {
        "required_columns": ["sequence"],
        "settings_map": {
            "sequence": "sequence",
        },
        "defaults": {},
    },
    "esmfold": {
        "required_columns": ["sequence"],
        "settings_map": {
            "sequence": "sequence",
        },
        "defaults": {},
    },
    "boltz": {
        "required_columns": ["sequence"],
        "settings_map": {
            "sequence": "sequence",
        },
        "defaults": {
            "inputFormat": "sequence",
        },
    },
    "chai": {
        "required_columns": ["sequence"],
        "settings_map": {
            "sequence": "sequence",
        },
        "defaults": {
            "inputFormat": "sequence",
        },
    },
}

# Tools that need file inputs (PDB, SDF) — can't submit from tabular data alone
FILE_DEPENDENT_TOOLS = {"diffdock", "gnina", "autodock-vina", "openmm", "prodigy"}


VALIDATOR_SYSTEM_PROMPT = """\
You are an expert computational biologist analyzing experimental results. \
Compare the computational results from multiple Tamarind Bio models against \
the claims made in a research paper. For each claim, determine if the \
computational evidence validates, partially validates, or contradicts it.

Be specific about metrics: RMSD values, binding energies, confidence scores, etc.
Return ONLY valid JSON.\
"""

VALIDATOR_USER_PROMPT = """\
Compare these computational results against the paper's claims.

PAPER TITLE: {paper_title}

PAPER CLAIMS:
{claims_json}

COMPUTATIONAL RESULTS:
{results_json}

SYNTHETIC DATA USED AS INPUTS:
{synthetic_inputs}

Return a JSON object:
{{
    "validation_id": "experiment_id_validation",
    "paper_title": "{paper_title}",
    "models_used": ["tool names"],
    "overall_reliability_score": 0.0 to 1.0,
    "claims_validated": [
        {{
            "claim": "text",
            "category": "category",
            "paper_evidence": "what paper says",
            "models_tested": ["tools"],
            "result": "validated|partially_validated|not_testable|invalidated",
            "confidence": "high|medium|low",
            "evidence": {{}},
            "conclusion": "1-2 sentences"
        }}
    ],
    "aggregate_results": {{
        "total_claims": 0,
        "validated": 0,
        "partially_validated": 0,
        "not_testable": 0,
        "invalidated": 0,
        "validation_rate": 0.0,
        "validation_summary": "X of Y claims supported"
    }},
    "final_verdict": {{
        "paper_validity": "strongly_supported|supported|mixed|weakly_supported|contradicted",
        "reliability_score": 0.0,
        "summary": "2-3 sentences"
    }}
}}\
"""


class ExperimentRunner:
    """
    Reads synthetic data from fs/runs/{run_id}/data/, maps rows directly
    to Tamarind jobs, submits, polls, downloads, and validates.
    """

    def __init__(
        self,
        anthropic_api_key: str | None = None,
        tamarind_api_key: str | None = None,
    ):
        self.anthropic = Anthropic(api_key=anthropic_api_key)
        self.tamarind = TamarindClient(api_key=tamarind_api_key)
        self.parser = PaperParser()

    # ── Load synthetic data from disk ────────────────────────────────

    def load_synthetic_data(self, run_id: str) -> dict:
        """
        Load synthetic data from fs/runs/{run_id}/data/.
        Returns dict with 'data' (rows), 'schema', and 'selected_model'.
        """
        data_dir = FS_RUNS_DIR / run_id / "data"
        if not data_dir.exists():
            raise FileNotFoundError(f"No data folder for run {run_id}. Run /synthetic/from-pdf first.")

        result = {}

        synth_file = data_dir / "synthetic_data.json"
        if synth_file.exists():
            result["data"] = json.loads(synth_file.read_text())
            result["total_rows"] = len(result["data"])

        schema_file = data_dir / "schema.json"
        if schema_file.exists():
            schema = json.loads(schema_file.read_text())
            result["schema"] = schema
            result["selected_model"] = schema.get("selected_model")

        return result

    # ── Parse paper ──────────────────────────────────────────────────

    def parse_paper(self, paper_path: str) -> str:
        return self.parser.parse(paper_path)

    # ── Build jobs directly from synthetic data ──────────────────────

    def build_jobs_from_data(self, synthetic_result: dict, experiment_id: str) -> dict:
        """
        Directly map synthetic data rows to Tamarind jobs.
        No Claude planning — just look at which columns are available
        and submit to matching tools.
        """
        rows = synthetic_result.get("data", [])
        if not rows:
            raise ValueError("No synthetic data rows to submit")

        columns = set(rows[0].keys())
        schema = synthetic_result.get("schema", {})

        # Determine which tools we can use based on available columns
        usable_tools = []
        for tool_name, config in TOOL_COLUMN_MAP.items():
            if all(col in columns for col in config["required_columns"]):
                usable_tools.append(tool_name)

        if not usable_tools:
            raise ValueError(f"No Tamarind tools match available columns: {columns}")

        logger.info(f"Available columns: {columns}")
        logger.info(f"Usable tools: {usable_tools}")

        # Build jobs: one job per unique sequence per tool
        jobs = []
        seen_sequences = {}  # track unique sequences to avoid duplicate jobs

        for row in rows:
            seq = row.get("sequence", "")
            if not seq or not isinstance(seq, str) or len(seq) < 10:
                continue

            # Skip if we already have a job for this sequence
            seq_key = seq[:50]  # use first 50 chars as key
            if seq_key in seen_sequences:
                continue
            seen_sequences[seq_key] = row

            row_idx = row.get("row_index", len(jobs))
            group = row.get("group", "")
            label = group or f"row{row_idx}"

            for tool_name in usable_tools:
                config = TOOL_COLUMN_MAP[tool_name]
                settings = dict(config["defaults"])

                # Map columns to settings
                for col_name, setting_name in config["settings_map"].items():
                    if col_name in row and row[col_name]:
                        settings[setting_name] = row[col_name]

                # Add ligand SMILES if available (for boltz/chai)
                if tool_name in ("boltz", "chai") and "ligand_smiles" in row:
                    settings["ligands"] = row["ligand_smiles"]

                job_name = f"{experiment_id}-{tool_name}-{label}".lower().replace(" ", "-").replace("_", "-")
                # Ensure job name is clean
                job_name = "".join(c for c in job_name if c.isalnum() or c == "-")

                jobs.append({
                    "jobName": job_name,
                    "type": tool_name,
                    "description": f"{tool_name} prediction for {label}",
                    "settings": settings,
                    "source_row_index": row_idx,
                    "source_group": group,
                })

        # Build a plan-like structure
        plan = {
            "experiment_id": experiment_id,
            "paper_title": schema.get("title", "Unknown"),
            "hypothesis": schema.get("hypothesis", ""),
            "total_jobs": len(jobs),
            "tools_used": list(set(j["type"] for j in jobs)),
            "stages": [
                {
                    "stage_number": 1,
                    "stage_name": "Structure Prediction & Analysis",
                    "description": "Direct submission of synthetic data to Tamarind models",
                    "jobs": jobs,
                }
            ],
        }

        logger.info(f"Built {len(jobs)} jobs from {len(seen_sequences)} unique sequences using tools: {usable_tools}")
        return plan

    # ── Submit jobs ──────────────────────────────────────────────────

    def submit_experiment(self, plan: dict, experiment_dir: Path) -> dict:
        """Submit all jobs to Tamarind Bio."""
        experiment_dir.mkdir(parents=True, exist_ok=True)
        inputs_dir = experiment_dir / "inputs"
        inputs_dir.mkdir(exist_ok=True)

        (experiment_dir / "pipeline.json").write_text(json.dumps(plan, indent=2))

        for stage in plan.get("stages", []):
            for job in stage.get("jobs", []):
                job_name = job["jobName"]
                tool_type = job["type"]
                settings = job.get("settings", {})

                # Save input for debugging
                input_file = inputs_dir / f"{tool_type}_{job_name}.json"
                input_file.write_text(json.dumps({
                    "jobName": job_name, "type": tool_type, "settings": settings,
                }, indent=2))

                try:
                    result = self.tamarind.submit_job(job_name, tool_type, settings)
                    job["submission_status"] = "submitted"
                    job["submission_response"] = result
                    logger.info(f"Submitted {job_name} ({tool_type})")
                except Exception as e:
                    job["submission_status"] = "failed"
                    job["submission_error"] = str(e)
                    logger.error(f"Failed to submit {job_name}: {e}")

        (experiment_dir / "pipeline.json").write_text(json.dumps(plan, indent=2))
        return plan

    # ── Poll for completion ──────────────────────────────────────────

    def poll_experiment(self, plan: dict, poll_interval: float = 30.0, max_wait: float = 7200.0) -> dict:
        """Poll submitted jobs until complete."""
        all_jobs = []
        for stage in plan.get("stages", []):
            for job in stage.get("jobs", []):
                if job.get("submission_status") == "submitted":
                    all_jobs.append(job)

        if not all_jobs:
            logger.warning("No submitted jobs to poll")
            return plan

        logger.info(f"Polling {len(all_jobs)} jobs...")
        start = time.time()
        pending = set(j["jobName"] for j in all_jobs)
        warned_not_found: set[str] = set()

        while pending and (time.time() - start) < max_wait:
            jobs_data = self.tamarind.get_jobs()
            job_statuses = {}
            for j in jobs_data.get("jobs", []):
                name = j.get("jobName") or j.get("name") or j.get("JobName")
                status = j.get("status") or j.get("JobStatus") or "unknown"
                if name:
                    job_statuses[name] = status

            newly_done = set()
            for job_name in pending:
                status = job_statuses.get(job_name)
                if status == "Complete":
                    logger.info(f"Job '{job_name}' completed")
                    newly_done.add(job_name)
                elif status == "Stopped":
                    logger.error(f"Job '{job_name}' stopped/failed")
                    newly_done.add(job_name)
                elif status is None:
                    if job_name not in warned_not_found:
                        logger.warning(f"Job '{job_name}' not found on Tamarind")
                        warned_not_found.add(job_name)
                else:
                    logger.info(f"Job '{job_name}': {status}")

            pending -= newly_done

            for stage in plan.get("stages", []):
                for job in stage.get("jobs", []):
                    if job["jobName"] in job_statuses:
                        job["final_status"] = job_statuses[job["jobName"]]

            if pending:
                time.sleep(poll_interval)

        if pending:
            logger.warning(f"Timed out waiting for: {pending}")

        return plan

    # ── Download results ─────────────────────────────────────────────

    def download_results(self, plan: dict, experiment_dir: Path) -> dict:
        outputs_dir = experiment_dir / "outputs"
        outputs_dir.mkdir(exist_ok=True)

        for stage in plan.get("stages", []):
            for job in stage.get("jobs", []):
                if job.get("final_status") != "Complete":
                    continue
                job_name = job["jobName"]
                try:
                    saved = self.tamarind.download_result_files(job_name, outputs_dir)
                    job["result_files"] = [str(p) for p in saved]
                    logger.info(f"Downloaded results for {job_name}")
                except Exception as e:
                    job["download_error"] = str(e)
                    logger.error(f"Failed to download {job_name}: {e}")

        (experiment_dir / "pipeline.json").write_text(json.dumps(plan, indent=2))
        return plan

    # ── Validate against paper ───────────────────────────────────────

    def validate_results(
        self, plan: dict, paper_text: str, experiment_dir: Path, synthetic_data_str: str = "",
    ) -> dict:
        outputs_dir = experiment_dir / "outputs"

        results = {}
        if outputs_dir.exists():
            for f in outputs_dir.glob("*.json"):
                try:
                    results[f.stem] = json.loads(f.read_text())
                except Exception:
                    pass

        if not results:
            logger.warning("No results to validate")
            return {"overall_reliability_score": 0, "claims_validated": [], "final_verdict": {"summary": "No results"}}

        prompt = VALIDATOR_USER_PROMPT.format(
            paper_title=plan.get("paper_title", "Unknown"),
            claims_json="[]",
            results_json=json.dumps(results, indent=2)[:20000],
            synthetic_inputs=synthetic_data_str[:5000] if synthetic_data_str else "N/A",
        )

        logger.info("Validating results with Claude Opus...")
        response = self.anthropic.messages.create(
            model="claude-opus-4-20250514",
            max_tokens=8192,
            system=VALIDATOR_SYSTEM_PROMPT,
            messages=[{"role": "user", "content": prompt}],
        )

        raw = response.content[0].text
        validation = self._parse_json(raw)
        (experiment_dir / "validation.json").write_text(json.dumps(validation, indent=2))
        logger.info(f"Validation score: {validation.get('overall_reliability_score', 'N/A')}")
        return validation

    # ── Full pipeline ────────────────────────────────────────────────

    def run(self, run_id: str, experiment_id: str | None = None) -> dict:
        """
        Assumes synthetic data already exists at fs/runs/{run_id}/data/.
        Directly maps data rows to Tamarind jobs and submits.
        """
        pdf_path = FS_RUNS_DIR / run_id / "original.pdf"

        paper_text = self.parse_paper(str(pdf_path))
        logger.info(f"Parsed paper: {len(paper_text)} chars")

        synthetic_result = self.load_synthetic_data(run_id)

        if not experiment_id:
            experiment_id = f"exp_{uuid.uuid4().hex[:8]}"

        plan = self.build_jobs_from_data(synthetic_result, experiment_id)

        experiment_dir = EXPERIMENTS_DIR / experiment_id
        experiment_dir.mkdir(parents=True, exist_ok=True)
        (experiment_dir / "paper_text.txt").write_text(paper_text[:50000])
        (experiment_dir / "run_id.txt").write_text(run_id)

        plan = self.submit_experiment(plan, experiment_dir)
        plan = self.poll_experiment(plan)
        plan = self.download_results(plan, experiment_dir)

        synthetic_data_str = json.dumps(synthetic_result.get("data", [])[:5], indent=2)
        validation = self.validate_results(plan, paper_text, experiment_dir, synthetic_data_str)

        return {
            "experiment_id": experiment_id,
            "experiment_dir": str(experiment_dir),
            "plan": plan,
            "validation": validation,
        }

    def plan_only(self, run_id: str) -> dict:
        """
        Build job plan from synthetic data without submitting.
        Shows what would be submitted to Tamarind.
        """
        synthetic_result = self.load_synthetic_data(run_id)

        experiment_id = f"exp_{uuid.uuid4().hex[:8]}"
        plan = self.build_jobs_from_data(synthetic_result, experiment_id)

        rows = synthetic_result.get("data", [])
        plan["synthetic_data_summary"] = {
            "num_rows": synthetic_result.get("total_rows", 0),
            "columns": list(rows[0].keys()) if rows else [],
            "selected_model": synthetic_result.get("selected_model"),
            "sample_rows": rows[:3],
        }
        return plan

    # ── Helpers ──────────────────────────────────────────────────────

    def _parse_json(self, raw: str) -> dict:
        text = raw.strip()
        if "```json" in text:
            start = text.index("```json") + 7
            end = text.index("```", start)
            text = text[start:end].strip()
        elif "```" in text:
            start = text.index("```") + 3
            end = text.index("```", start)
            text = text[start:end].strip()
        else:
            first = text.find("{")
            last = text.rfind("}")
            if first != -1 and last != -1:
                text = text[first:last + 1]
        return json.loads(text)
